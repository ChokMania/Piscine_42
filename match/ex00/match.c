int		match(char *s1, char *s2)
{
	int i;
	int j;

	i = -1;
	j = -1;
	while (s1[++i] && s2[++j])
	{
		while (s2[j] == '*')
			j++;
		if (s2[j] == '\0')
			return (1);
		while (s1[i] != s2[j] && s2[j - 1] == '*')
		{
			i++;
			if (s1[i] == '\0')
				return (0);
		}
		if (s1[i] != s2[j])
			return (0);
	}
	while (s2[j] == '*')
		j++;
	if (s1[i] == '\0' && s2[j] == '\0')
		return (1);
	return (0);
}
