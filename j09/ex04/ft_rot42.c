char	*ft_rot42(char *str)
{
	int i;
	int j;

	i = 0;
	while (str[i] != '\0')
	{
		j = 16;
		while (j != 0)
		{
			if (str[i] == 'z' || str[i] == 'Z')
				str[i] -= 25;
			else if ((str[i] >= 'a' && str[i] <= 'z') ||
					(str[i] >= 'A' && str[i] <= 'Z'))
				str[i]++;
			j--;
		}
		i++;
	}
	return (str);
}
